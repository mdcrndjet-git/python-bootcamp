# Multiple String Formatting using placeholder

# Ask the user for two numbers
instruction = "First Number: "
first_number = int(input(instruction))
instruction = "Second Number: "
second_number = int(input(instruction))

# Then, print the sum
sum = first_number + second_number

# show solution/formula
solution = f"{sum} = {first_number} + {second_number}"
print(solution)